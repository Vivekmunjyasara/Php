<?php
$x=10;

echo 'Normal value of x:- '.$x;
echo '<br><br>';
//post increment
echo 'post increment:- ';
$x++;
echo $x;
echo '<br><br>';

//pre increment
echo 'pre increment:- ';
++$x;
echo $x;
echo '<br><br>';

//post decrement
echo 'post decrement:- ';
$x--;
echo $x;
echo '<br><br>';

//pre decrement
echo 'pre decrement:- ';
--$x;
echo $x;
echo '<br><br>';

?>